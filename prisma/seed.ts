function loadCoordOverrides(): Record<string, { latitude: number; longitude: number }> {
  try {
    const p = path.join(process.cwd(), "prisma", "coord-overrides.json");
    if (fs.existsSync(p)) return JSON.parse(fs.readFileSync(p, "utf-8"));
  } catch {
    // ignore
  }
  return {};
}

